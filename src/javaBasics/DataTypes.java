package javaBasics;

public class DataTypes 
{
	public static void main(String[] args)
	{
	//Primitive datatypes
	int x; //declaration
	//int x=10; //initialization
	
	//Derived datatypes
	String y="Shwetha"; //declared as a variable
	System.out.println(y.length()); //used as an object
	
	//Array declaration
	int z[]=new int[5];
	
	//Array initialization
	int x1[]= {12,67,-90,0};
	System.out.println(x1.length);
	
	char c[]= {'s','h','w','e','t','h','a'};
	System.out.println(c.length);
	}
}
